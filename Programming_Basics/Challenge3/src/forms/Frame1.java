    /**
     * Method to add new products to the database.
     */
    public void addProduct(){
        // Set an information message.
        String message = "Se ha agregado el producto con Ã©xito.";
        // Set the information icon.
        int icon = JOptionPane.INFORMATION_MESSAGE;
        // Verifi if any field is empty.
        if(codeTxt.getText().isEmpty() || nameTxt.getText().isEmpty() || 
                priceTxt.getText().isEmpty() || amountTxt.getText().isEmpty()){
            message = "Todos los campos son obligatorios";
            icon = JOptionPane.WARNING_MESSAGE;
        }
        else{
            // Check if the code exists in the database.
            if(db.verificarExistencia(Integer.valueOf(codeTxt.getText()))){
                // Set an error message.
                message = 
                        "El cÃ³digo del producto ya existe en la base de datos.";
                // Set an error icon.
                icon = JOptionPane.ERROR_MESSAGE;
            }
            else{
                // Add the new dog to the database.
                db.agregar(new Product(codeTxt.getText(), nameTxt.getText(),
                    priceTxt.getText(), amountTxt.getText()));
                clearFields();
            }
        }
        // Display the message.
        JOptionPane.showMessageDialog(this, message, "Alerta", icon);
    }
    
