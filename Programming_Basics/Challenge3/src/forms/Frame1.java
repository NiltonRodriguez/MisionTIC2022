            // Send confirmation Dialog.
            int choice = JOptionPane.showConfirmDialog(this, 
                "Â¿Desea eliminar el registro?", 
                "Alerta", JOptionPane.YES_NO_OPTION, 
                JOptionPane.WARNING_MESSAGE);
